'''
Базовая цена путевки турфирмы «Отдыхай как дома» – 10000 рублей, 
 - при добавлении завтраков цена увеличивается на 10000, 
 - при добавлении обедов на 15000, 
 - за выбор корпуса с бассейном тоже нужно доплатить 5000. 

Написать программу, содержащую функцию для расчета полной стоимости путевки. 
Пользователь самостоятельно выбирает все опции.
'''

def calculate_vacation_cost():
    base_price = 10000
    breakfast = input("Добавить завтраки? (да/нет): ")
    lunch = input("Добавить обеды? (да/нет): ")
    pool = input("Выбрать корпус с бассейном? (да/нет): ")

    total_cost = base_price
    if breakfast.lower() == "да":
        total_cost += 10000
    if lunch.lower() == "да":
        total_cost += 15000
    if pool.lower() == "да":
        total_cost += 5000

    print(f"Полная стоимость путевки: {total_cost} рублей")

calculate_vacation_cost()