'''
Спортивный центр "Прыг-скок" автоматизирует работу судей. 
Теперь им нужна программа для выдачи случайного стартового номера участникам. 
Судья вводит общее количество участников, программа выводит один случайный номер. 
Для каждого участника программа запускается заново, повтор номеров не учитывать.
'''

import random

# Генерация случайного стартового номера
total_participants = int(input("Введите общее количество участников: "))

if total_participants <= 0:
    print("Количество участников должно быть больше нуля.")
else:
    start_number = random.randint(1, total_participants)
    print(f"Случайный стартовый номер: {start_number}")