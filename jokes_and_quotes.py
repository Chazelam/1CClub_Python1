import random

def get_joke():
    jokes = [
        "Почему программисты так хорошо работают ночью? Потому что ночью нет багов, только фичи!",
        "Что говорит один бит другому? У тебя такой хороший потенциал!",
        "Почему утки так хорошо пишут код? Потому что они любят ""кря""ивые скобки!",
        "Тестировщик заходит в бар. Заказывает 0 пива, -1 пива, слона, и 'пиво' в кавычках. Все работает."
    ]
    return random.choice(jokes)

def get_quote():
    quotes = [
        "Будь собой. Прочие роли уже заняты. - Оскар Уайльд",
        "Вчера ты сказал 'завтра'. Сделай это сегодня! - Шайа ЛаБаф",
        "Жизнь – это 10% того, что с нами происходит, и 90% того, как мы на это реагируем. - Чарльз Р. Суиндолл",
        "Каждый день – это новый шанс изменить свою жизнь."
    ]
    return random.choice(quotes)

def jokes_and_quotes():
    while True:
        print("\nМеню:")
        print("1. Рассказать анекдот")
        print("2. Поделиться цитатой")
        print("3. Выйти")
        choice = input("Выберите действие (1-3): ")

        if choice == "1":
            print(f"Анекдот: {get_joke()}")
        elif choice == "2":
            print(f"Цитата: {get_quote()}")
        elif choice == "3":
            print("Выход из модуля.")
            break
        else:
            print("Неверный выбор. Попробуйте снова.")

if __name__ == "__main__":
    jokes_and_quotes()